using System;
using System.Runtime.CompilerServices;

namespace jQueryApi.UI.Widgets {

    /// <summary>
    /// Options for use with ProgressBar.
    /// </summary>
    [Imported]
    [IgnoreNamespace]
    [NamedValues]
    public enum ProgressBarOption {

        /// <summary>
        /// Disables the progressbar if set to true.
        /// </summary>
        Disabled,

        /// <summary>
        /// The value of the progressbar.
        /// </summary>
        Value
    }
}
